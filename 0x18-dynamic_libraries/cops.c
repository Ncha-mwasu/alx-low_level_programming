/**
 * add - adds two operands
 * @a: first operand
 * @b: second operand
 *
 * Return: operation carried on operand
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtract two operands
 * @a: first operand
 * @b: second operand
 *
 * Return: operation carried on operand
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies two operands
 * @a: first operand
 * @b: second operand
 *
 * Return: operation carried on operand
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides two operands
 * @a: first operand
 * @b: second operand
 *
 * Return: operation carried on operand
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - finds the modulus of two operands
 * @a: first operand
 * @b: second operand
 *
 * Return: operation carried on operand
 */

int mod(int a, int b)
{
	return (a % b);
}
